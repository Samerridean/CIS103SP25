from turtle import*
def circles():
    pencolor('#ffb31a')
    penup()
    setposition(-69,-200)
    pensize(10)
    pendown()
    circle(300)
    pencolor('#5c00e6')
    penup()
    setposition(-200,100)
    pensize(5)
    fillcolor('blue')
    begin_fill()
    pendown()
    circle(100)
    pencolor('#ff6666')
    end_fill()
    penup()
    setposition(76,100)
    fillcolor('yellow')
    begin_fill()
    pendown()
    circle(100)
    end_fill()
def lines():
    pencolor('black')
    penup()
    setposition(-200,0)
    pensize(1)
    fillcolor('black')
    begin_fill()
    pendown()
    forward(275)
    right(90)
    forward(20)
    right(90)
    forward(275)
    right(90)
    forward(20)
    end_fill()
    pencolor('red')
    penup()
    setposition(-200,-100)
    pensize(10)
    pendown()
    right(90)
    forward(275)
    right(140)
    forward(200)
    right(85)
    forward(182)
def main():
    hideturtle()
    circles()
    lines()
main()
